<p> 
John Smith (Letters, Wednesday 29 May) says that we have the best sites for wind energy, why are we not investing in more?  The simple answer is that none of the renewables that politicians talk about are economically viable.  As an example, as Centrica have pointed out, offshore wind energy costs £3m per megawatt more than gas or nuclear.   No intelligent person would choose to feed the National Grid with turbines that are idle for 70” of the time and that can go weeks on end without generating a single volt.
</p>
<p>  
The objections to civil nuclear power are due to ignorance and Mr Smith’s concerns about nuclear waste  have no basis in fact.  He is obviously unaware that there are millions of tons of radioactive rock in the world, which is just as well since without, this planet would be too cold for human habitation.  To add a few tons of nuclear waste will make no difference.  There are several methods of treating nuclear waste (see www.greenfocus.co.uk) but in fact a cheap and sensible way would be to encase it in concrete, wrap it in polythene and drop it in one of the canyons what go thousands of feet deep in the oceans.  It is unlikely to leak out but if it did, the dilution factor would render it innocuous. 
</p>
