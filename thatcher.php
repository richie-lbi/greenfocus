<h1>
Margaret Thatcher (Letter to the journal)
</h1>
<p>Dear  Mr  Aitken</p>
<p>The party at Easington of the Durham Miners Association during the funeral of Baroness Thatcher will not impress inward investors nor potential visitors to the region.</p>
<p>Mrs Thatcher brought Nissan to Wearside - the most successful inward investment of all time.</p>
<p>Claims that she wanted to destroy the coal industry are nonsense.  What killed the coal industry was the decision to allow natural gas to be used in power stations.  This appalled scientists, of which Mrs Thatcher was one, because most of the heat in a power station is lost.  The sensible way to use gas is in combined heat and power (CHP) or microgeneration where power and heat are utilised.</p>
<p>The advantage claimed for gas was that it was "greener" than coal.  However a mix of coal and nuclear stations would be great (emit less CO2) than gas.  Arthur Scargill could have prevented the use of gas in power stations but he was only interested in overthrowing a legitimately elected government.</p>
<p>The hardships suffered in 1984 by miners and their families were a result of the betrayal by Scargill of union members.  Those who revile Baroness Thatcher and praise Scargill are invincibly ignorant.</p>
<p>Yours faithfully</p>

<p>T R MANLEY</p>
