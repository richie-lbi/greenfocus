BIOGRAPHY 
Tom Manley was among the first to recognise the potential of reverse osmosis to recover metals [British Patent 814 602]. 
In Prague in 1965 he presented the first FTIR spectrum of plastics. </P>
<p>Today FTIR is routinely used to provide an economical method of separating mixed waste for recycling. FTIR may be used to monitor the effluent from incinerators, cement works and other plant where waste materials are burnt to recover energy. 
  He has worked on economic methods of recycling waste such as car batteries (for polypropylene); pulverised fuel ash (PFA); asbestos and straw. 
  He was awarded a Waverley gold medal prize for his work in irradiation of plastics while he was involved in nuclear power production - the least polluting method of producing electricity.  </P>
<p>He was awarded a prize by the Institution of Electrical Engineers for his work on epoxy resins. Epoxy resins replace hydrocarbon oils and polychlorinated biphenyls (PCB) and also by extending mains electricity reduce the use of diesel generators.</P>
